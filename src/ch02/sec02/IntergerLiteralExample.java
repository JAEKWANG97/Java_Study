package ch02.sec02;

public class IntergerLiteralExample {
    public static void main(String[] args){
        int var1 =0b1011; // 11
        int var2 =0206; // 6 + 2*64
        int var3 =365;
        int var4 =0xb3; //16진수


        System.out.println(var1);
        System.out.println(var2);
        System.out.println(var3);
        System.out.println(var4);
    }
}
